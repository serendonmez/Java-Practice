package Day4_Soru;

public class C03_3eTamBolenler {


    //Soru 1'den 100'e kadar olan sayılardan 3 ile tam bölünenleri yazdırınız


    public static void main(String[] args) {


        for (int i = 1; i <=100 ; i++) {
            if (i%3==0){
                System.out.print(i+" ");
            }

        }
    }




}
